
function thFilter(ent) {
    var ckPaymentName = 'thFilter';
    Cookies.set(ckPaymentName, "", { expires: 1 });
    var rs = $("#" + ent.id).data("slug");
    Cookies.set(ckPaymentName, rs);
    $("#frmFilter").submit();
}
